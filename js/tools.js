/**
 * 转小写
 * @param value
 * @returns {string}
 */
exports.toLower = function(value) {
    return value.toLowerCase() + '!!';
}
/**
 * 转大写
 * @param value
 * @returns {string}
 */
exports.toUpper = function(value) {
    return value.toUpperCase() + '???';
}

